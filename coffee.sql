-- 판매현황
SELECT
    SALENO,
    P.PCODE,
    SALEDATE,
    SCODE,
    P.NAME,
    AMOUNT,
    COST*AMOUNT
FROM
    TBL_PRODUCT_01 P,
    TBL_SALELIST_01 SL
WHERE
    P.PCODE = SL.PCODE;

-- 매장펼 판매액
SELECT
    SH.SCODE,
    SNAME,
    SUM(P.COST*SL.AMOUNT)
FROM
    TBL_PRODUCT_01 P,
    TBL_SHOP_01 SH,
    TBL_SALELIST_01 SL
WHERE
    SH.SCODE = SL.SCODE AND
    SL.PCODE = P.PCODE
GROUP BY
    SH.SCODE, SNAME
ORDER BY
    SH.SCODE, SNAME;

-- 상품별 판매액
SELECT
    P.PCODE,
    NAME,
    SUM(AMOUNT*COST)
FROM
    TBL_PRODUCT_01 P,
    TBL_SALELIST_01 SL
WHERE
    P.PCODE = SL.PCODE
GROUP BY
    P.PCODE,
    NAME;